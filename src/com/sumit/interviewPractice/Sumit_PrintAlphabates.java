package com.sumit.interviewPractice;

public class Sumit_PrintAlphabates
{
	public static void main(String[] args) 
	{
		char ch; 
		
		System.out.println("Big");
		
		for (ch = 'A'; ch <= 'Z' ; ch ++)
		{
			System.out.print(ch+" ");
		}
		System.out.println("\n\n");
		System.out.println("Small");
		for (ch = 'a'; ch <= 'z' ; ch ++)
		{
			System.out.print(ch+" ");
		}
		
	}

}
